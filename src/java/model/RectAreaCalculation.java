package model;

/**
 *
 * @author benja_000
 */
public class RectAreaCalculation {

    private int length;
    private int width;

    public RectAreaCalculation(int length, int width) {
        this.length = length;
        this.width = width;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int rectangleCalculation(int length, int width) {
        return length * width;
    }
}
